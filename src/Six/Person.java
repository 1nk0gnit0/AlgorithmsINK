package Six;

public class Person {
    public String name;
    public int id;
    public int age;


    public Person(String name, int id, int age) {
        this.name = name;
        this.id = id;
        this.age = age;
    }

    public Person(int id) {
        this.id = id;
    }

    public Person() {
    }
}
